/* 38) Escreva um programa que mostre na tela a seguinte contagem:  
6 7 8 9 10 11 Acabou!  */
public class TrigesimoOitavoExercicio {
    public static void main(String[] args){
        int num = 5;
        
        while(num < 11){
            num++;
            System.out.print(num + " ");
        }
        System.out.print("Acabou!");
    }
}
